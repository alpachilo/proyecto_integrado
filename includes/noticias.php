    <div class="panel panel-info">
      <div class="panel-heading">
        <h3 class="panel-title">Regata Internacional de Piragüismo Rio Guadiana<span class="label label-primary fecha">22/09/2016</span></h3>
      </div>
      <div class="panel-body">
            <p><img src="../Imagenes/cartel_pira.jpg" class="img-responsive" alt="iberman" width="650" height="500"><br>
            <p style="font-weight: bold;">Reglamento específico:</p>
            <p align="justify">
            1.- Salida:<br><br>
            La salida estará establecida frente al Puerto de Alcoutim (Portugal).
            Las salidas se harán por grupos. La primera a las 12,00 horas españolas, que abarca las piraguas de Kayak de mar, Cadete, C1 y C2; la segunda 30 minutos más tarde, que comprende, las de Competición o pista y los sprinter.
            Los horarios pueden alterarse a juicio de la Organización atendiendo a las condiciones climatológicas, de las mareas o de otra índole.
            La salida se dará desde el agua, en la línea comprendida entre una baliza y el pantalán del propio Puerto Deportivo.
            El Juez de salida dará la señal de la carrera mediante una bocina.<br><br>
            2.- Control:<br><br>
            El control de Meta se cerrará para las embarcaciones que tenga la meta establecida en Ayamonte, será de 4 horas y 30 minutos después de haberse dado la primera salida. Si las condiciones del río son adversas puede ampliarse este tiempo a juicio de la organización. Todos aquellos piragüistas que lleguen fuera de control no optarán a las diferentes clasificaciones. El control de meta de la categoría cadete, C1 y C2 cierra a las 1 hora y 45 minutos después de la salida.
            Al término de la misma en el Pabellón Ciudad de Ayamonte, habrá una comida de convivencia de los participantes y organización. Saludos piragüeros.<p>
      </div>
